    {
        // Extract values from request
        $value = $request->input('value');
        $type = $request->input('type');
        
        // Add values to table
        $contact = new Contact;
        
        $contact->value = $value;
        $contact->type = $type;
        $contact->save();
        
        // Respond with a JSON object similar to request
        return response()->json([
            'value' => $value,
            'type' => $type
        ]);
    }

